-- ==========================================================
-- Business Question 5:
-- Which cities have the highest number of churned customers?
-- ==========================================================

SELECT TOP 10
    City,
    COUNT(*) AS Churned_Customers
FROM Telco_customer_churn_clean
WHERE [Churn Label] = 'Yes'
GROUP BY City
ORDER BY Churned_Customers DESC;


-- ==========================================================
-- Business Question 6:
-- What are the most common reasons customers churn?
-- ==========================================================

