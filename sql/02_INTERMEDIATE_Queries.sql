-- ==========================================================
-- Business Question 9:
-- What is the average Customer Lifetime Value (CLTV)
-- of churned vs active customers?
-- ==========================================================

SELECT
    [Churn Label],
    ROUND(AVG(CLTV),2) AS Average_CLTV
FROM Telco_customer_churn_clean
GROUP BY [Churn Label];


-- ==========================================================
-- Business Question 10:
-- How much revenue is generated by each contract type?
-- ==========================================================

SELECT
    Contract,
    ROUND(SUM([Total Charges]),2) AS Total_Revenue
FROM Telco_customer_churn_clean
GROUP BY Contract
ORDER BY Total_Revenue DESC;