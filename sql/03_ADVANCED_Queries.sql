-- ==========================================================
-- CUSTOMER CHURN PREDICTION & RETENTION ANALYTICS
-- ADVANCED SQL QUERIES
-- ==========================================================

-- ==========================================================
-- Business Question 1:
-- Rank cities based on the number of churned customers.
-- ==========================================================

SELECT
    City,
    COUNT(*) AS Churned_Customers,
    DENSE_RANK() OVER (
        ORDER BY COUNT(*) DESC
    ) AS City_Rank
FROM Telco_customer_churn_clean
WHERE [Churn Label] = 'Yes'
GROUP BY City;


-- ==========================================================
-- Business Question 2:
-- Rank contract types based on total revenue generated.
-- ==========================================================

SELECT
    Contract,
    ROUND(SUM([Total Charges]),2) AS Total_Revenue,
    RANK() OVER (
        ORDER BY SUM([Total Charges]) DESC
    ) AS Revenue_Rank
FROM Telco_customer_churn_clean
GROUP BY Contract;


-- ==========================================================
-- Business Question 3:
-- Identify high-value customers who have churned.
-- (CLTV greater than overall average)
-- ==========================================================

WITH AvgCLTV AS
(
    SELECT AVG(CLTV) AS Avg_CLTV
    FROM Telco_customer_churn_clean
)

SELECT
    CustomerID,
    City,
    Contract,
    CLTV
FROM Telco_customer_churn_clean
CROSS JOIN AvgCLTV
WHERE
    [Churn Label]='Yes'
    AND CLTV > Avg_CLTV
ORDER BY CLTV DESC;


-- ==========================================================
-- Business Question 4:
-- Which combination of Contract and Internet Service
-- experiences the highest churn?
-- ==========================================================

SELECT
    Contract,
    [Internet Service],
    COUNT(*) AS Churned_Customers
FROM Telco_customer_churn_clean
WHERE [Churn Label]='Yes'
GROUP BY
    Contract,
    [Internet Service]
ORDER BY Churned_Customers DESC;


-- ==========================================================
-- Business Question 5:
-- Rank churn reasons by frequency.
-- ==========================================================

SELECT
    [Churn Reason],
    COUNT(*) AS Total_Customers,
    DENSE_RANK() OVER
    (
        ORDER BY COUNT(*) DESC
    ) AS Reason_Rank
FROM Telco_customer_churn_clean
WHERE [Churn Label]='Yes'
GROUP BY [Churn Reason];


-- ==========================================================
-- Business Question 6:
-- Find customers paying above average monthly charges.
-- ==========================================================

SELECT
    CustomerID,
    [Monthly Charges]
FROM Telco_customer_churn_clean
WHERE [Monthly Charges] >
(
    SELECT AVG([Monthly Charges])
    FROM Telco_customer_churn_clean
)
ORDER BY [Monthly Charges] DESC;


-- ==========================================================
-- Business Question 7:
-- Identify high-risk customer segments.
-- (Month-to-Month + Fiber Optic + Electronic Check)
-- ==========================================================

SELECT
    CustomerID,
    City,
    Contract,
    [Internet Service],
    [Payment Method],
    [Monthly Charges]
FROM Telco_customer_churn_clean
WHERE
    Contract='Month-to-month'
    AND [Internet Service]='Fiber Optic'
    AND [Payment Method]='Electronic Check';


-- ==========================================================
-- Business Question 8:
-- Calculate cumulative revenue generated by customers
-- ordered by Total Charges.
-- ==========================================================

SELECT
    CustomerID,
    [Total Charges],
    SUM([Total Charges]) OVER
    (
        ORDER BY [Total Charges]
    ) AS Running_Revenue
FROM Telco_customer_churn_clean;


-- ==========================================================
-- Business Question 9:
-- Compare churn percentage for each contract type.
-- ==========================================================

SELECT
    Contract,
    COUNT(*) AS Total_Customers,
    SUM(
        CASE
            WHEN [Churn Label]='Yes'
            THEN 1
            ELSE 0
        END
    ) AS Churned_Customers,
    ROUND(
        SUM(
            CASE
                WHEN [Churn Label]='Yes'
                THEN 1
                ELSE 0
            END
        )*100.0/COUNT(*),
        2
    ) AS Churn_Rate_Percentage
FROM Telco_customer_churn_clean
GROUP BY Contract
ORDER BY Churn_Rate_Percentage DESC;


-- ==========================================================
-- Business Question 10:
-- Identify the Top 10 highest CLTV customers.
-- ==========================================================

SELECT TOP 10
    CustomerID,
    City,
    Contract,
    CLTV
FROM Telco_customer_churn_clean
ORDER BY CLTV DESC;